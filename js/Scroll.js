$(document).ready(function(){
    $(window).scroll(function(){
        if ($(this).scrollTop() < 400){
            $(".header").css({"opacity" : "1"})
        }
        else{
            $(".header").css({"opacity" : "0"})
        }
    })
})



$(document).ready(function(){
    $(window).scroll(function(){
        if ($(this).scrollTop() < 400){
            $(".header").css({"z-index" : "99",})
        }
        else{
            $(".header").css({"z-index" : "-1"})
        }
    })
})

